    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         String uname=usertxt.getText();
       String password=String.valueOf(userpass.getPassword());
         if(uname.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this , "Username / Password should not be empty" , "Error" , JOptionPane.ERROR_MESSAGE);
        }
       String query = "SELECT * FROM `registeruser` WHERE `uname` =? AND `password` =?";
        try {
            pst = con.prepareStatement(query);
            pst.setString(1, uname);
            pst.setString(2, password);
            
            rs = pst.executeQuery();
            
            if(rs.next()){
                 setVisible(false);
      new userview().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"SignUp first");
            }
        } catch (SQLException ex) {
            Logger.getLogger(loginpage.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

