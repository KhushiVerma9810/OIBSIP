        uname=u_name.getText();
        password=String.valueOf(txtpass.getPassword());
        con_pass=String.valueOf(txtconpass.getPassword());
        contact_no=txtcontact.getText();
        streamm = txtstr.getText();
        email = txtemail.getText();
        
        if ( uname.isEmpty() || password.isEmpty() || con_pass.isEmpty() || contact_no.isEmpty() || email.isEmpty() || streamm.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all the fields");
          }
        else if(!password.equals(con_pass))
        {
            JOptionPane.showMessageDialog(null, "Password not matched");
        }
        else if(contact_no.length() > 10)
         {
            JOptionPane.showMessageDialog(null ,"Contact No should be 10 digit");
         }
        else if(validemail(email)== false){
            JOptionPane.showMessageDialog(null ,"please input valid email");
        }
        else{
              try {
                  pst = con.prepareStatement("INSERT INTO `registeruser`( `uname`, `password`, `stream`, `email`) VALUES (?,?,?,?)");
                   pst.setString(1, uname);
         pst.setString(2,password );
        pst.setString(3, streamm);
        pst.setString(4, email);
        
         int k = pst.executeUpdate();

        if(k==1){
            JOptionPane.showMessageDialog(this, "Registered Successfully!!");
        }
              else{
            JOptionPane.showMessageDialog(this, "Something went wrong");
        }
              } catch (SQLException ex) {
                  Logger.getLogger(admin.class.getName()).log(Level.SEVERE, null, ex);
              }
        }
